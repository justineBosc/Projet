  Individu::~Individu(void){
  }

// ===========================================================================
//                               Public Methods
// ===========================================================================

  void Individu::mutation(float Pmut){
	  float r = rand()/(double)RAND_MAX;
	  if (r < Pmut){
		  if (get_G()==0) { 
			  set_G(1); 
		  }
		  else if (get_G()==1) {
			  set_G(0); 
		  }
	  }
  }
  
  void Individu::death(float Pdeath){
    float r = rand()/(double)RAND_MAX;
    if (r < Pdeath){
      set_vivant(0);
    }
  }

